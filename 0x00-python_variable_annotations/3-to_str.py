#!/usr/bin/env python3
"""
Basic annotations - to_str function.

This module defines a type-annotated function `to_str` that takes a float `n`
and returns its string representation.
"""

def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of `n`.
    """
    return str(n)
